create materialized view public.green_button_after_hours_pct_monthly_mv as
with interval_points as (
  select
    org_id,
    building_id,
    interval_start_utc at time zone 'UTC' as interval_start_local,
    source_usage_point_id,
    (value_wh * 3600.0) / nullif(duration_seconds, 0) / 1000.0 as kw
  from public.green_button_intervals_analytics_v
  where duration_seconds > 0
),
dedup_usage_point_interval as (
  select
    org_id,
    building_id,
    date_trunc('month', interval_start_local)::date as month_start,
    date(interval_start_local) as local_day,
    interval_start_local,
    source_usage_point_id,
    max(kw)::numeric(18,6) as usage_point_kw
  from interval_points
  group by
    org_id,
    building_id,
    date_trunc('month', interval_start_local)::date,
    date(interval_start_local),
    interval_start_local,
    source_usage_point_id
),
building_interval as (
  select
    org_id,
    building_id,
    month_start,
    local_day,
    interval_start_local,
    extract(isodow from interval_start_local)::int as isodow,
    extract(hour from interval_start_local)::int as hour_local,
    extract(minute from interval_start_local)::int as minute_local,
    sum(usage_point_kw)::numeric(18,6) as building_kw
  from dedup_usage_point_interval
  group by org_id, building_id, month_start, local_day, interval_start_local
),
two_am_points as (
  select
    org_id,
    building_id,
    month_start,
    building_kw
  from building_interval
  where isodow between 1 and 5
    and hour_local = 2
    and minute_local = 0
),
daily_peaks as (
  select
    org_id,
    building_id,
    month_start,
    local_day,
    max(building_kw)::numeric(18,6) as daily_peak_kw
  from building_interval
  where isodow between 1 and 5
  group by org_id, building_id, month_start, local_day
),
two_am_agg as (
  select
    org_id,
    building_id,
    month_start,
    avg(building_kw)::numeric(18,6) as avg_2am_kw_weekdays,
    count(*)::integer as two_am_intervals_count
  from two_am_points
  group by org_id, building_id, month_start
),
daily_peak_agg as (
  select
    org_id,
    building_id,
    month_start,
    max(daily_peak_kw)::numeric(18,6) as peak_weekday_kw
  from daily_peaks
  group by org_id, building_id, month_start
)
select
  t.org_id,
  t.building_id,
  t.month_start,
  t.avg_2am_kw_weekdays,
  p.peak_weekday_kw,
  t.two_am_intervals_count
from two_am_agg t
join daily_peak_agg p
  on p.org_id = t.org_id
 and p.building_id = t.building_id
 and p.month_start = t.month_start
with no data;



create materialized view public.green_button_load_shape_monthly_mv as
with interval_points as (
  select
    i.org_id,
    i.building_id,
    i.interval_start_utc,
    i.interval_start_utc at time zone coalesce(
      nullif(to_jsonb(b) ->> 'timezone', ''),
      nullif(to_jsonb(b) ->> 'time_zone', ''),
      nullif(to_jsonb(b) ->> 'tz', ''),
      'America/Chicago'
    ) as interval_start_local,
    (i.value_wh * 3600.0) / nullif(i.duration_seconds, 0) / 1000.0 as kw
  from public.green_button_intervals_analytics_v i
  left join public.buildings b
    on b.id = i.building_id
  where i.duration_seconds > 0
),
weekday_points as (
  select
    org_id,
    building_id,
    date_trunc('month', interval_start_local)::date as month_start,
    date(interval_start_local) as local_day,
    extract(hour from interval_start_local)::integer as local_hour,
    floor(extract(minute from interval_start_local) / 15.0)::integer as quarter_hour,
    kw
  from interval_points
  where extract(isodow from interval_start_local) between 1 and 5
),
raw_monthly as (
  select
    org_id,
    building_id,
    month_start,
    (local_hour * 4 + quarter_hour)::smallint as slot_index,
    lpad(local_hour::text, 2, '0') || ':' || lpad((quarter_hour * 15)::text, 2, '0') as hhmm,
    avg(kw)::numeric(18,6) as avg_kw,
    count(*)::integer as intervals_count,
    count(distinct local_day)::integer as weekday_days_count
  from weekday_points
  where quarter_hour between 0 and 3
  group by org_id, building_id, month_start, local_hour, quarter_hour
),
combined as (
  select
    org_id,
    building_id,
    month_start,
    slot_index,
    hhmm,
    avg_kw,
    intervals_count,
    weekday_days_count,
    2 as source_rank
  from raw_monthly
  union all
  select
    org_id,
    building_id,
    month_start,
    slot_index,
    hhmm,
    avg_kw,
    intervals_count,
    weekday_days_count,
    1 as source_rank
  from public.green_button_load_shape_monthly_history
),
final_ranked as (
  select
    org_id,
    building_id,
    month_start,
    slot_index,
    hhmm,
    avg_kw,
    intervals_count,
    weekday_days_count,
    row_number() over (
      partition by org_id, building_id, month_start, slot_index
      order by source_rank desc
    ) as rn
  from combined
)
select
  org_id,
  building_id,
  month_start,
  slot_index,
  hhmm,
  avg_kw,
  intervals_count,
  weekday_days_count
from final_ranked
where rn = 1
with no data;



