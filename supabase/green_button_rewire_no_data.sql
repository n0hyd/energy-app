create materialized view public.green_button_demand_facts_monthly_mv as
with normalized as (
  select
    b.org_id,
    bm.building_id,
    date_trunc('month', bm.bill_month)::date as month_start,
    bm.adjusted_demand_kw,
    bm.tariff_min_kw,
    bm.ratchet_kw,
    bm.billing_demand_kw,
    bm.demand_charge_usd
  from public.bill_months bm
  join public.buildings b
    on b.id = bm.building_id
  where bm.bill_month is not null
    and bm.building_id is not null
),
aggregated as (
  select
    org_id,
    building_id,
    month_start,
    max(adjusted_demand_kw)::numeric(18,6) as adjusted_demand_kw,
    max(tariff_min_kw)::numeric(18,6) as tariff_min_kw,
    max(ratchet_kw)::numeric(18,6) as ratchet_kw,
    max(billing_demand_kw)::numeric(18,6) as billing_demand_kw,
    sum(demand_charge_usd)::numeric(18,6) as demand_charge_usd_total,
    sum(billing_demand_kw) filter (where billing_demand_kw > 0 and demand_charge_usd is not null)::numeric(18,6)
      as billing_demand_kw_for_rate
  from normalized
  group by org_id, building_id, month_start
)
select
  org_id,
  building_id,
  month_start,
  adjusted_demand_kw,
  tariff_min_kw,
  ratchet_kw,
  billing_demand_kw,
  demand_charge_usd_total,
  case
    when billing_demand_kw_for_rate > 0
      then (demand_charge_usd_total / billing_demand_kw_for_rate)::numeric(18,6)
    else null
  end as effective_demand_rate_usd_per_kw
from aggregated
with no data;




