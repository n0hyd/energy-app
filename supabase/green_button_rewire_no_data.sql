begin
  for obj in
    select unnest(array[
      'green_button_monthly_energy_mv',
      'green_button_monthly_top_peaks_mv',
      'green_button_startup_intensity_monthly_mv',
      'green_button_peak_timing_monthly_mv',
      'green_button_after_hours_load_monthly_mv',
      'green_button_after_hours_pct_monthly_mv',
      'green_button_load_shape_monthly_mv',
      'green_button_weekend_ops_monthly_mv',
      'green_button_demand_facts_monthly_mv'
    ]::text[])
  loop
    if exists (
      select 1
      from pg_class c
      join pg_namespace n on n.oid = c.relnamespace
      where n.nspname = 'public'
        and c.relname = obj
        and c.relkind = 'm'
    ) then
      execute format('drop materialized view public.%I', obj);
    elsif exists (
      select 1
      from pg_class c
      join pg_namespace n on n.oid = c.relnamespace
      where n.nspname = 'public'
        and c.relname = obj
        and c.relkind = 'v'
    ) then
      execute format('drop view public.%I', obj);
    end if;
  end loop;
end
$do$;


create table if not exists public.green_button_monthly_energy_history (
  id bigint generated by default as identity primary key,
  org_id uuid not null references public.organizations(id) on delete cascade,
  building_id uuid not null references public.buildings(id) on delete cascade,
  month_start date not null,
  kwh_total numeric(18,6) not null check (kwh_total >= 0),
  source text not null default 'archived',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint green_button_monthly_energy_history_unique
    unique (org_id, building_id, month_start)
);

create index if not exists green_button_monthly_energy_history_org_month_idx
  on public.green_button_monthly_energy_history (org_id, month_start desc);

create materialized view public.green_button_monthly_energy_mv as
with raw_monthly as (
  select
    org_id,
    building_id,
    date_trunc('month', interval_start_utc)::date as month_start,
    sum(value_wh) / 1000.0 as kwh_total
  from public.green_button_intervals_analytics_v
  group by
    org_id,
    building_id,
    date_trunc('month', interval_start_utc)
),
combined as (
  select
    org_id,
    building_id,
    month_start,
    kwh_total,
    2 as source_rank
  from raw_monthly
  union all
  select
    org_id,
    building_id,
    month_start,
    kwh_total,
    1 as source_rank
  from public.green_button_monthly_energy_history
),
final_ranked as (
  select
    org_id,
    building_id,
    month_start,
    kwh_total,
    row_number() over (
      partition by org_id, building_id, month_start
      order by source_rank desc
    ) as rn
  from combined
)
select
  org_id,
  building_id,
  month_start,
  kwh_total,
  now() as refreshed_at
from final_ranked
where rn = 1
with no data;



create materialized view public.green_button_monthly_top_peaks_mv as
with raw_ranked as (
  select
    org_id,
    building_id,
    date_trunc('month', interval_start_utc)::date as month_start,
    (value_wh * 3600.0) / nullif(duration_seconds, 0) / 1000.0 as peak_kw,
    interval_start_utc as peak_interval_start_utc,
    row_number() over (
      partition by org_id, building_id, date_trunc('month', interval_start_utc)
      order by
        (value_wh * 3600.0) / nullif(duration_seconds, 0) / 1000.0 desc,
        interval_start_utc desc
    ) as peak_rank
  from public.green_button_intervals_analytics_v
  where duration_seconds > 0
),
raw_top as (
  select
    org_id,
    building_id,
    month_start,
    peak_rank,
    peak_kw,
    peak_interval_start_utc,
    2 as source_rank
  from raw_ranked
  where peak_rank <= 5
),
history_top as (
  select
    org_id,
    building_id,
    month_start,
    peak_rank,
    peak_kw,
    peak_interval_start_utc,
    1 as source_rank
  from public.green_button_monthly_top_peaks_history
),
combined as (
  select * from raw_top
  union all
  select * from history_top
),
final_ranked as (
  select
    org_id,
    building_id,
    month_start,
    peak_rank,
    peak_kw,
    peak_interval_start_utc,
    row_number() over (
      partition by org_id, building_id, month_start, peak_rank
      order by source_rank desc
    ) as rn
  from combined
)
select
  org_id,
  building_id,
  month_start,
  peak_rank,
  peak_kw,
  peak_interval_start_utc
from final_ranked
where rn = 1
with no data;



create materialized view public.green_button_startup_intensity_monthly_mv as
with interval_points as (
  select
    org_id,
    building_id,
    interval_start_utc,
    interval_start_utc at time zone 'UTC' as interval_start_local,
    (value_wh * 3600.0) / nullif(duration_seconds, 0) / 1000.0 as kw
  from public.green_button_intervals_analytics_v
  where duration_seconds > 0
),
ramp_points as (
  select
    org_id,
    building_id,
    date_trunc('month', interval_start_local)::date as month_start,
    interval_start_utc as ramp_interval_start_utc,
    interval_start_local,
    kw - lag(kw) over (
      partition by org_id, building_id
      order by interval_start_utc
    ) as ramp_kw
  from interval_points
),
ranked as (
  select
    org_id,
    building_id,
    month_start,
    ramp_kw as max_ramp_kw,
    ramp_interval_start_utc,
    row_number() over (
      partition by org_id, building_id, month_start
      order by ramp_kw desc, ramp_interval_start_utc desc
    ) as rn
  from ramp_points
  where extract(hour from interval_start_local) >= 4
    and extract(hour from interval_start_local) < 9
    and ramp_kw is not null
    and ramp_kw > 0
),
raw_monthly as (
  select
    org_id,
    building_id,
    month_start,
    max_ramp_kw,
    ramp_interval_start_utc
  from ranked
  where rn = 1
),
combined as (
  select org_id, building_id, month_start, max_ramp_kw, ramp_interval_start_utc
  from raw_monthly
  union all
  select org_id, building_id, month_start, max_ramp_kw, ramp_interval_start_utc
  from public.green_button_startup_intensity_monthly_history
),
final_ranked as (
  select
    org_id,
    building_id,
    month_start,
    max_ramp_kw,
    ramp_interval_start_utc,
    row_number() over (
      partition by org_id, building_id, month_start
      order by max_ramp_kw desc, ramp_interval_start_utc desc nulls last
    ) as rn
  from combined
)
select
  org_id,
  building_id,
  month_start,
  max_ramp_kw,
  ramp_interval_start_utc
from final_ranked
where rn = 1
with no data;



