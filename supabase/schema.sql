      on b.id = i.building_id
    where i.org_id = p_org_id
      and i.interval_start_utc < v_cutoff
      and i.duration_seconds > 0
  ),
  old_peak_timing_ranked as (
    select
      org_id,
      building_id,
      date_trunc('month', interval_start_local)::date as month_start,
      extract(hour from interval_start_local)::integer as peak_hour_local,
      row_number() over (
        partition by org_id, building_id, date(interval_start_local)
        order by kw desc, interval_start_utc desc
      ) as rn
    from old_peak_timing_points
  ),
  old_peak_timing_histogram as (
    select
      org_id,
      building_id,
      month_start,
      peak_hour_local,
      count(*)::integer as daily_peak_count
