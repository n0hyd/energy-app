create materialized view public.green_button_startup_intensity_monthly_mv as
with interval_points as (
  select
    i.org_id,
    i.building_id,
    i.interval_start_utc,
    i.interval_start_utc at time zone 'UTC' as interval_start_local,
    (i.value_wh * 3600.0) / nullif(i.duration_seconds, 0) / 1000.0 as kw
  from public.green_button_intervals_analytics_v i
  where i.duration_seconds > 0
),
ramp_points as (
  select
    org_id,
    building_id,
    date_trunc('month', interval_start_local)::date as month_start,
    interval_start_utc as ramp_interval_start_utc,
    interval_start_local,
    kw - lag(kw) over (
      partition by org_id, building_id
      order by interval_start_utc
    ) as ramp_kw
  from interval_points
),
ranked as (
  select
    org_id,
    building_id,
    month_start,
    ramp_kw as max_ramp_kw,
    ramp_interval_start_utc,
    row_number() over (
      partition by org_id, building_id, month_start
      order by ramp_kw desc, ramp_interval_start_utc desc
