      building_id,
      date_trunc('month', interval_start_local)::date as month_start,
      date(interval_start_local) as local_day,
      extract(hour from interval_start_local)::integer as local_hour,
      floor(extract(minute from interval_start_local) / 15.0)::integer as quarter_hour,
      (value_wh * 3600.0) / nullif(duration_seconds, 0) / 1000.0 as kw
    from interval_points
    where extract(isodow from interval_start_local) between 1 and 5
  ),
  raw_monthly as (
    select
      org_id,
      building_id,
      month_start,
      (local_hour * 4 + quarter_hour)::smallint as slot_index,
      lpad(local_hour::text, 2, '0') || ':' || lpad((quarter_hour * 15)::text, 2, '0') as hhmm,
      avg(kw)::numeric(18,6) as avg_kw,
      count(*)::integer as intervals_count,
      count(distinct local_day)::integer as weekday_days_count
    from weekday_points
    where quarter_hour between 0 and 3
      and (
        coalesce(array_length(v_months_checked, 1), 0) = 0
        or month_start = any(v_months_checked)
      )
    group by org_id, building_id, month_start, local_hour, quarter_hour
  ),
  combined as (
    select
      org_id,
      building_id,
      month_start,
      slot_index,
      hhmm,
      avg_kw,
      intervals_count,
      weekday_days_count,
      2 as source_rank
    from raw_monthly
    union all
    select
      h.org_id,
      h.building_id,
      h.month_start,
      h.slot_index,
      h.hhmm,
      h.avg_kw,
      h.intervals_count,
      h.weekday_days_count,
      1 as source_rank
    from public.green_button_load_shape_monthly_history h
    where h.org_id = p_org_id
      and (p_building_id is null or h.building_id = p_building_id)
      and (
        coalesce(array_length(v_months_checked, 1), 0) = 0
        or h.month_start = any(v_months_checked)
      )
  ),
  final_ranked as (
    select
      org_id,
      building_id,
      month_start,
      slot_index,
      hhmm,
      avg_kw,
      intervals_count,
      weekday_days_count,
      row_number() over (
        partition by org_id, building_id, month_start, slot_index
        order by source_rank desc
      ) as rn
    from combined
  ),
  scoped_rows as (
    select
      org_id,
      building_id,
      month_start,
      slot_index,
      hhmm,
      avg_kw,
      intervals_count,
      weekday_days_count
    from final_ranked
    where rn = 1
  )
  insert into public.green_button_load_shape_monthly_cache (
    org_id,
    building_id,
    month_start,
    slot_index,
    hhmm,
    avg_kw,
    intervals_count,
    weekday_days_count,
    refreshed_at
  )
  select
    org_id,
    building_id,
    month_start,
    slot_index,
    hhmm,
    avg_kw,
    intervals_count,
    weekday_days_count,
    now()
  from scoped_rows
  on conflict (org_id, building_id, month_start, slot_index)
  do update set
    hhmm = excluded.hhmm,
    avg_kw = excluded.avg_kw,
    intervals_count = excluded.intervals_count,
    weekday_days_count = excluded.weekday_days_count,
    refreshed_at = excluded.refreshed_at;

  get diagnostics v_rows_upserted = row_count;

  if coalesce(array_length(v_months_checked, 1), 0) > 0 and coalesce(p_expected_weekday_intervals, 0) > 0 then
    select count(*)::bigint
      into v_rows_for_scope
    from public.green_button_load_shape_monthly_cache
    where org_id = p_org_id
      and (p_building_id is null or building_id = p_building_id)
      and month_start = any(v_months_checked);

    v_verified := v_rows_for_scope > 0;
  end if;

  return jsonb_build_object(
    'rows_upserted', v_rows_upserted,
    'rows_for_scope', v_rows_for_scope,
    'months_checked', coalesce(v_months_checked, array[]::date[]),
    'expected_weekday_intervals', coalesce(p_expected_weekday_intervals, 0),
    'verified', v_verified,
    'cache_table', 'green_button_load_shape_monthly_cache'
  );
end;
$$;

