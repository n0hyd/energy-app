create or replace function public.snapshot_green_button_historical_monthly_kpis(
  p_org_id uuid,
  p_snapshot_at timestamptz default now(),
  p_month_starts date[] default null,
  p_building_id uuid default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_snapshot_id uuid := gen_random_uuid();
  v_complete_months bigint := 0;
  v_energy_rows bigint := 0;
  v_peak_rows bigint := 0;
  v_night_rows bigint := 0;
  v_weekend_rows bigint := 0;
  v_shape_rows bigint := 0;
begin
  if p_org_id is null then
    raise exception 'p_org_id is required';
  end if;

  with building_tz as (
    select
      b.id as building_id,
      b.org_id,
      coalesce(
        nullif(to_jsonb(b) ->> 'timezone', ''),
        nullif(to_jsonb(b) ->> 'time_zone', ''),
        nullif(to_jsonb(b) ->> 'tz', ''),
        'America/Chicago'
      ) as building_timezone
    from public.buildings b
    where b.org_id = p_org_id
      and (p_building_id is null or b.id = p_building_id)
  ),
  interval_agg as (
    select
      i.org_id,
      i.building_id,
      i.interval_start_utc,
      max(i.duration_seconds)::integer as duration_seconds,
      sum(i.value_wh)::numeric(18,6) as value_wh
    from public.green_button_intervals_analytics_v i
    where i.org_id = p_org_id
      and (p_building_id is null or i.building_id = p_building_id)
      and i.duration_seconds > 0
    group by i.org_id, i.building_id, i.interval_start_utc
  ),
  base_points as (
    select
      ia.org_id,
      ia.building_id,
      bt.building_timezone,
      ia.interval_start_utc,
      ia.duration_seconds,
      ia.value_wh,
      ia.interval_start_utc at time zone bt.building_timezone as interval_start_local,
      date_trunc('month', ia.interval_start_utc at time zone bt.building_timezone)::date as month_start
    from interval_agg ia
    join building_tz bt
      on bt.org_id = ia.org_id
     and bt.building_id = ia.building_id
    where ia.org_id = p_org_id
      and (p_building_id is null or ia.building_id = p_building_id)
      and ia.duration_seconds > 0
      and (
        coalesce(array_length(p_month_starts, 1), 0) = 0
        or date_trunc('month', ia.interval_start_utc at time zone bt.building_timezone)::date = any(p_month_starts)
      )
  ),
  month_bounds as (
    select distinct
      org_id,
      building_id,
      building_timezone,
      month_start,
      (month_start::timestamp at time zone building_timezone) as month_start_utc,
      ((month_start + interval '1 month')::timestamp at time zone building_timezone) as month_end_utc
    from base_points
  ),
  month_coverage as (
    select
      mb.org_id,
      mb.building_id,
      mb.building_timezone,
      mb.month_start,
      (mb.month_start + interval '1 month')::date as month_end,
      mb.month_start_utc,
      mb.month_end_utc,
      extract(epoch from (mb.month_end_utc - mb.month_start_utc))::integer as expected_seconds,
      coalesce(sum(bp.duration_seconds), 0)::integer as observed_seconds,
      count(distinct date(bp.interval_start_local))::integer as covered_days_count,
      extract(day from ((mb.month_start + interval '1 month')::date - interval '1 day'))::integer as expected_days_count
    from month_bounds mb
    left join base_points bp
      on bp.org_id = mb.org_id
     and bp.building_id = mb.building_id
     and bp.interval_start_utc >= mb.month_start_utc
     and bp.interval_start_utc < mb.month_end_utc
    group by
      mb.org_id,
      mb.building_id,
      mb.building_timezone,
      mb.month_start,
      mb.month_start_utc,
      mb.month_end_utc
  ),
  complete_months as (
    select
      org_id,
      building_id,
      building_timezone,
      month_start,
      month_end,
      month_start_utc,
      month_end_utc,
      expected_seconds,
      observed_seconds
    from month_coverage
    where observed_seconds >= expected_seconds
      and covered_days_count >= expected_days_count
  ),
  scoped_points as (
    select
      bp.org_id,
      bp.building_id,
      cm.building_timezone,
      cm.month_start,
