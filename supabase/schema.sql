    'energy_rows_inserted', v_energy_rows,
    'peak_rows_inserted', v_peak_rows,
    'night_baseload_rows_inserted', v_night_rows,
    'weekend_ops_rows_inserted', v_weekend_rows,
    'load_shape_rows_inserted', v_shape_rows,
    'timezone_basis', 'stored interval clock basis (interval_start_utc at time zone UTC)'
  );
end;
$$;

