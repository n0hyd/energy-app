create materialized view public.green_button_monthly_energy_mv as
with raw_monthly as (
  select
    org_id,
    building_id,
    date_trunc('month', interval_start_utc)::date as month_start,
    sum(value_wh) / 1000.0 as kwh_total
  from public.green_button_intervals_analytics_v
  group by
    org_id,
    building_id,
    date_trunc('month', interval_start_utc)
),
combined as (
  select
    org_id,
    building_id,
    month_start,
    kwh_total,
    2 as source_rank
  from raw_monthly
  union all
  select
    org_id,
    building_id,
    month_start,
    kwh_total,
    1 as source_rank
  from public.green_button_monthly_energy_history
),
final_ranked as (
  select
    org_id,
    building_id,
    month_start,
    kwh_total,
    row_number() over (
      partition by org_id, building_id, month_start
      order by source_rank desc
    ) as rn
  from combined
)
select
  org_id,
  building_id,
  month_start,
  kwh_total,
  now() as refreshed_at
from final_ranked
where rn = 1;

