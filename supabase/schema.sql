create or replace function public.refresh_green_button_monthly_peak_cache(
  p_org_id uuid,
  p_building_id uuid default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_rows bigint := 0;
begin
  if p_org_id is null then
    raise exception 'p_org_id is required';
  end if;

  delete from public.green_button_monthly_peak_cache
  where org_id = p_org_id
    and (p_building_id is null or building_id = p_building_id);

  with raw_ranked as (
    select
      i.org_id,
      i.building_id,
      date_trunc(
        'month',
        i.interval_start_utc at time zone 'UTC'
      )::date as month_start,
      i.interval_start_utc,
      (i.value_wh * 3600.0) / nullif(i.duration_seconds, 0) / 1000.0 as peak_kw,
      row_number() over (
        partition by
          i.org_id,
          i.building_id,
          date_trunc(
            'month',
            i.interval_start_utc at time zone 'UTC'
          )
        order by
          (i.value_wh * 3600.0) / nullif(i.duration_seconds, 0) / 1000.0 desc,
          i.interval_start_utc desc
      ) as rn
    from public.green_button_intervals_analytics_v i
    where i.org_id = p_org_id
      and (p_building_id is null or i.building_id = p_building_id)
      and i.duration_seconds > 0
  ),
  raw_monthly as (
    select
      org_id,
      building_id,
      month_start,
      peak_kw,
      interval_start_utc as peak_interval_start_utc
    from raw_ranked
    where rn = 1
  ),
  combined as (
    select org_id, building_id, month_start, peak_kw, peak_interval_start_utc
    from raw_monthly
    union all
    select org_id, building_id, month_start, peak_kw, peak_interval_start_utc
    from public.green_button_monthly_peak_history
    where org_id = p_org_id
      and (p_building_id is null or building_id = p_building_id)
  ),
  final_ranked as (
    select
      org_id,
      building_id,
      month_start,
      peak_kw,
      peak_interval_start_utc,
      row_number() over (
        partition by org_id, building_id, month_start
        order by peak_kw desc, peak_interval_start_utc desc nulls last
      ) as rn
    from combined
  ),
  final_rows as (
    select org_id, building_id, month_start, peak_kw, peak_interval_start_utc
    from final_ranked
    where rn = 1
  ),
  inserted as (
    insert into public.green_button_monthly_peak_cache (
      org_id,
      building_id,
      month_start,
      peak_kw,
      peak_interval_start_utc,
      refreshed_at
    )
    select
      org_id,
      building_id,
      month_start,
      peak_kw,
      peak_interval_start_utc,
      now()
    from final_rows
    returning 1
  )
  select count(*)::bigint into v_rows from inserted;

  return jsonb_build_object(
    'org_id', p_org_id,
    'building_id', p_building_id,
    'rows_refreshed', v_rows
  );
end;
$$;

