      v_retry_used := true;
      refresh materialized view public.green_button_load_shape_monthly_mv;

      select count(*)::bigint
        into v_rows_for_scope
      from public.green_button_load_shape_monthly_mv
      where org_id = p_org_id
        and building_id = p_building_id
        and month_start = any(v_months_checked);
    end if;

    v_verified := v_rows_for_scope > 0;
  end if;

  return jsonb_build_object(
    'rows_refreshed', v_rows_refreshed,
    'rows_for_scope', v_rows_for_scope,
    'months_checked', coalesce(v_months_checked, array[]::date[]),
