    insert into public.green_button_after_hours_load_monthly_history (
      org_id,
      building_id,
      month_start,
      avg_night_min_kw,
      nights_count,
      source,
      updated_at
    )
    select
      org_id,
      building_id,
      month_start,
      avg_night_min_kw,
      nights_count,
      'archived',
      now()
    from old_after_hours_monthly
    on conflict (org_id, building_id, month_start) do update
      set avg_night_min_kw = excluded.avg_night_min_kw,
          nights_count = excluded.nights_count,
          source = 'archived',
          updated_at = now()
    returning 1
  ),
  old_load_shape_points as (
    select
      i.org_id,
      i.building_id,
      date_trunc(
        'month',
        i.interval_start_utc at time zone 'UTC'
      )::date as month_start,
      date(
        i.interval_start_utc at time zone 'UTC'
      ) as local_day,
      extract(
        hour from (
          i.interval_start_utc at time zone 'UTC'
        )
      )::integer as local_hour,
      floor(
        extract(
          minute from (
            i.interval_start_utc at time zone 'UTC'
          )
        ) / 15.0
      )::integer as quarter_hour,
      max(i.duration_seconds)::integer as duration_seconds,
      sum(i.value_wh)::numeric(18,6) as value_wh
    from public.green_button_intervals_analytics_v i
    left join public.buildings b
      on b.id = i.building_id
    where i.org_id = p_org_id
      and i.interval_start_utc < v_cutoff
      and i.duration_seconds > 0
      and extract(
        isodow from (
          i.interval_start_utc at time zone 'UTC'
        )
      ) between 1 and 5
    group by
      i.org_id,
      i.building_id,
      date_trunc(
        'month',
        i.interval_start_utc at time zone 'UTC'
      )::date,
      date(i.interval_start_utc at time zone 'UTC'),
      extract(
        hour from (
          i.interval_start_utc at time zone 'UTC'
        )
      )::integer,
      floor(
        extract(
          minute from (
            i.interval_start_utc at time zone 'UTC'
          )
        ) / 15.0
      )::integer
  ),
  old_load_shape_monthly as (
    select
      org_id,
      building_id,
      month_start,
      (local_hour * 4 + quarter_hour)::smallint as slot_index,
      lpad(local_hour::text, 2, '0') || ':' || lpad((quarter_hour * 15)::text, 2, '0') as hhmm,
      avg((value_wh * 3600.0) / nullif(duration_seconds, 0) / 1000.0)::numeric(18,6) as avg_kw,
      count(*)::integer as intervals_count,
      count(distinct local_day)::integer as weekday_days_count
    from old_load_shape_points
    where quarter_hour between 0 and 3
    group by org_id, building_id, month_start, local_hour, quarter_hour
  ),
  load_shape_upserted as (
    insert into public.green_button_load_shape_monthly_history (
      org_id,
      building_id,
      month_start,
      slot_index,
      hhmm,
      avg_kw,
      intervals_count,
      weekday_days_count,
      source,
      updated_at
    )
    select
      org_id,
      building_id,
      month_start,
      slot_index,
      hhmm,
      avg_kw,
      intervals_count,
      weekday_days_count,
      'archived',
      now()
    from old_load_shape_monthly
    on conflict (org_id, building_id, month_start, slot_index) do update
      set hhmm = excluded.hhmm,
          avg_kw = excluded.avg_kw,
          intervals_count = excluded.intervals_count,
          weekday_days_count = excluded.weekday_days_count,
          source = 'archived',
          updated_at = now()
    returning 1
  ),
  old_top_ranked as (
    select
      org_id,
      building_id,
      date_trunc('month', interval_start_utc)::date as month_start,
      (value_wh * 3600.0) / nullif(duration_seconds, 0) / 1000.0 as peak_kw,
      interval_start_utc as peak_interval_start_utc,
      row_number() over (
        partition by org_id, building_id, date_trunc('month', interval_start_utc)
        order by
          (value_wh * 3600.0) / nullif(duration_seconds, 0) / 1000.0 desc,
          interval_start_utc desc
      ) as peak_rank
    from public.green_button_intervals_analytics_v
    where org_id = p_org_id
      and interval_start_utc < v_cutoff
      and duration_seconds > 0
  ),
  old_top_monthly as (
    select
      org_id,
      building_id,
      month_start,
      peak_rank,
      peak_kw,
      peak_interval_start_utc
    from old_top_ranked
    where peak_rank <= 5
  ),
  top_peaks_upserted as (
    insert into public.green_button_monthly_top_peaks_history (
      org_id,
      building_id,
      month_start,
      peak_rank,
      peak_kw,
      peak_interval_start_utc,
      source,
      updated_at
    )
    select
      org_id,
      building_id,
      month_start,
      peak_rank,
      peak_kw,
      peak_interval_start_utc,
      'archived',
      now()
    from old_top_monthly
    on conflict (org_id, building_id, month_start, peak_rank) do update
      set peak_kw = excluded.peak_kw,
          peak_interval_start_utc = excluded.peak_interval_start_utc,
          source = 'archived',
          updated_at = now()
    returning 1
  ),
  old_weekend_points as (
    select
      i.org_id,
      i.building_id,
      i.interval_start_utc at time zone 'UTC' as interval_start_local,
      i.value_wh / 1000.0 as kwh
    from public.green_button_intervals_analytics_v i
    left join public.buildings b
      on b.id = i.building_id
    where i.org_id = p_org_id
      and i.interval_start_utc < v_cutoff
      and i.duration_seconds > 0
  ),
  old_weekend_daily_totals as (
    select
      org_id,
      building_id,
      date_trunc('month', interval_start_local)::date as month_start,
      date(interval_start_local) as local_day,
      sum(kwh)::numeric(18,6) as day_kwh
    from old_weekend_points
    group by org_id, building_id, date_trunc('month', interval_start_local), date(interval_start_local)
  ),
  old_weekend_monthly as (
    select
      org_id,
      building_id,
      month_start,
      avg(day_kwh) filter (where extract(isodow from local_day) in (6, 7))::numeric(18,6) as weekend_kwh_avg,
      avg(day_kwh) filter (where extract(isodow from local_day) between 1 and 5)::numeric(18,6) as weekday_kwh_avg,
      count(*) filter (where extract(isodow from local_day) in (6, 7))::integer as weekend_days_count,
      count(*) filter (where extract(isodow from local_day) between 1 and 5)::integer as weekday_days_count
    from old_weekend_daily_totals
    group by org_id, building_id, month_start
  ),
  weekend_ops_upserted as (
    insert into public.green_button_weekend_ops_monthly_history (
      org_id,
      building_id,
      month_start,
      weekend_kwh_avg,
      weekday_kwh_avg,
      weekend_days_count,
      weekday_days_count,
      source,
      updated_at
    )
    select
      org_id,
      building_id,
      month_start,
      weekend_kwh_avg,
      weekday_kwh_avg,
      weekend_days_count,
      weekday_days_count,
      'archived',
      now()
    from old_weekend_monthly
    on conflict (org_id, building_id, month_start) do update
      set weekend_kwh_avg = excluded.weekend_kwh_avg,
          weekday_kwh_avg = excluded.weekday_kwh_avg,
          weekend_days_count = excluded.weekend_days_count,
          weekday_days_count = excluded.weekday_days_count,
          source = 'archived',
          updated_at = now()
    returning 1
  )
  select
    (select count(*)::bigint from upserted),
    (select count(*)::bigint from energy_upserted),
    (select count(*)::bigint from startup_upserted),
    (select count(*)::bigint from peak_timing_upserted),
    (select count(*)::bigint from after_hours_upserted),
    (select count(*)::bigint from load_shape_upserted),
    (select count(*)::bigint from top_peaks_upserted),
    (select count(*)::bigint from weekend_ops_upserted)
  into v_upserted, v_energy_upserted, v_startup_upserted, v_peak_timing_upserted, v_after_hours_upserted, v_load_shape_upserted, v_top_peaks_upserted, v_weekend_ops_upserted;

  delete from public.green_button_intervals
  where org_id = p_org_id
    and interval_start_utc < v_cutoff;

  get diagnostics v_deleted = row_count;

  return jsonb_build_object(
    'org_id', p_org_id,
    'cutoff_utc', v_cutoff,
    'months_kept', p_keep_months,
    'history_upserted', v_upserted,
    'energy_history_upserted', v_energy_upserted,
    'startup_history_upserted', v_startup_upserted,
    'peak_timing_history_upserted', v_peak_timing_upserted,
    'after_hours_history_upserted', v_after_hours_upserted,
    'load_shape_history_upserted', v_load_shape_upserted,
    'top_peaks_history_upserted', v_top_peaks_upserted,
    'weekend_ops_history_upserted', v_weekend_ops_upserted,
    'rows_deleted', v_deleted
  );
end;
$$;




