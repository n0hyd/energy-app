create view public.green_button_intervals_analytics_v as
select *
from public.green_button_intervals
where coalesce(is_duplicate, false) = false
  and coalesce(stream_role, 'other') in ('consumption', 'other')
  and value_wh is not null;

create view public.green_button_kpi_guardrail_v as
with stream_points as (
  select
    org_id,
    building_id,
    interval_start_utc,
    canonical_stream_id,
    sum(value_wh)::numeric(18,6) as stream_value_wh,
    sum(value_wh) / 1000.0 as stream_kwh,
    sum((value_wh * 3600.0) / nullif(duration_seconds, 0) / 1000.0)::numeric(18,6) as stream_kw
  from public.green_button_intervals_analytics_v
  group by org_id, building_id, interval_start_utc, canonical_stream_id
),
building_points as (
  select
    org_id,
    building_id,
    interval_start_utc,
    sum(stream_value_wh)::numeric(18,6) as building_value_wh,
    sum(stream_kwh)::numeric(18,6) as building_kwh,
    sum(stream_kw)::numeric(18,6) as building_kw,
    count(*)::integer as canonical_stream_count
  from stream_points
  group by org_id, building_id, interval_start_utc
)
select
  s.org_id,
  s.building_id,
  s.interval_start_utc,
  s.canonical_stream_id,
  s.stream_value_wh,
  s.stream_kwh,
  s.stream_kw,
  b.building_value_wh,
  b.building_kwh,
  b.building_kw,
  b.canonical_stream_count
from stream_points s
join building_points b
  on b.org_id = s.org_id
 and b.building_id = s.building_id
 and b.interval_start_utc = s.interval_start_utc;

