      bp.value_wh,
      (bp.value_wh * 3600.0) / nullif(bp.duration_seconds, 0) / 1000.0 as kw,
      (bp.value_wh / 1000.0) as kwh
    from base_points bp
    join complete_months cm
      on cm.org_id = bp.org_id
     and cm.building_id = bp.building_id
     and bp.interval_start_utc >= cm.month_start_utc
     and bp.interval_start_utc < cm.month_end_utc
